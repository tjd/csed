package basics;

public class ArithmeticLimits {

    public static void main(String[] args) {
        System.out.printf("Smallest byte = %s, Largest byte = %s\n",
                Byte.MIN_VALUE, Byte.MAX_VALUE);
        System.out.printf("Smallest int = %s, Largest int = %s\n",
                Integer.MIN_VALUE, Integer.MAX_VALUE);
        System.out.printf("Smallest long = %s, Largest long = %s\n",
                Long.MIN_VALUE, Long.MAX_VALUE);
        System.out.printf("Smallest float = %s, Largest float = %s\n",
                Float.MIN_VALUE, Float.MAX_VALUE);
        System.out.printf("Smallest double = %s, Largest double = %s\n",
                Double.MIN_VALUE, Double.MAX_VALUE);
    }

}
